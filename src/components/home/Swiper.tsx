import { FaQuoteRight } from "react-icons/fa";
import { register } from "swiper/element/bundle";

import { testimonial } from "./Data";

register();

const Swiper = () => {
  const { persons } = testimonial;

  return (
    <swiper-container
      className="w-full pb-16"
      slides-per-view="1"
      speed="500"
      loop="true"
      css-mode="true"
      navigation="true"
      pagination="true"
      // scrollbar="true"
    >
      {persons.map((person, index) => (
        <swiper-slide key={index}>
          <div>
            <span>
              <FaQuoteRight />
            </span>
            <p>{person.message}</p>

            <div>
              <img
                src={person.avatar}
                alt="customer image"
                width={100}
                height={100}
              />
              <div>
                <h4>{person.name}</h4>
                <h5>{person.title}</h5>
              </div>
            </div>
          </div>
        </swiper-slide>
      ))}
    </swiper-container>
  );
};

export default Swiper;
